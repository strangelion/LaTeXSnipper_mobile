// Pre-compiled regex for fast replacement
let _regex = null;
function getRegex() {
  if (!_regex) {
    // Sort by key length descending to match longer phrases first
    const sorted = [...T2S.keys()].sort((a, b) => b.length - a.length);
    _regex = new RegExp(sorted.map(s => s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')).join('|'), 'g');
  }
  return _regex;
}

export function simplifyText(text) {
  if (!text) return text;
  const re = getRegex();
  return text.replace(re, match => T2S.get(match) || match);
}
